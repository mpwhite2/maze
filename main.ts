function move (speed: number, sprite: Sprite) {
    thrustDir = ViewAngle
    thrustDirRads = thrustDir * 3.1416 / 180
    thrustX = speed * Math.cos(thrustDirRads)
    thrustY = speed * Math.sin(thrustDirRads)
    sprite.setVelocity(thrustX, thrustY)
}
